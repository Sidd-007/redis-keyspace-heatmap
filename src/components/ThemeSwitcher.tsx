export function ThemeSwitcher() {
  const [currentTheme, setCurrentTheme] = useState('Default');
  const [isOpen, setIsOpen] = useState(false);
  const [mounted, setMounted] = useState(false);

  useEffect(() => {
    setMounted(true);
    const savedTheme = localStorage.getItem('redis-heatmap-theme');
    if (savedTheme) {
      applyTheme(savedTheme);
    }
  }, []);

  const applyTheme = (themeName: string) => {
    if (!mounted) return;

    const theme = themes.find(t => t.name === themeName);
    if (!theme) return;

    // Create or update the theme style element
    let themeStyle = document.getElementById('theme-style');
    if (!themeStyle) {
      themeStyle = document.createElement('style');
      themeStyle.id = 'theme-style';
      document.head.appendChild(themeStyle);
    }
    themeStyle.textContent = theme.css;

    setCurrentTheme(themeName);
    localStorage.setItem('redis-heatmap-theme', themeName);
  };

  const copyThemeCSS = (themeName: string) => {
    if (!mounted) return;

    const theme = themes.find(t => t.name === themeName);
    if (!theme) return;

    navigator.clipboard.writeText(theme.css).then(() => {
      // You could add a toast notification here
      console.log('Theme CSS copied to clipboard!');
    });
  };

  // Don't render until mounted to prevent hydration mismatch
  if (!mounted) {
    return (
      <Button variant="outline" size="sm" className="gap-2">
        <Palette className="w-4 h-4" />
        Theme
      </Button>
    );
  }

  return (
    <Dialog open={isOpen} onOpenChange={setIsOpen}>
      <Tooltip>
        <TooltipTrigger asChild>
          <DialogTrigger asChild>
            <Button variant="outline" size="sm" className="gap-2">
              <Palette className="w-4 h-4" />
              Theme
            </Button>
          </DialogTrigger>
        </TooltipTrigger>
        <TooltipContent>
          <p>Theme Settings</p>
          <p className="text-xs opacity-80">Customize the application appearance</p>
        </TooltipContent>
      </Tooltip>
      <DialogContent className="sm:max-w-md">
        <DialogHeader>
          <DialogTitle>Choose Theme</DialogTitle>
        </DialogHeader>
        <div className="space-y-4">
          <p className="text-sm text-muted-foreground">
            Select a theme to apply to your Redis Heatmap application. All themes are fully compatible with shadcn/ui.
          </p>
          <div className="grid gap-2">
            {themes.map((theme) => (
              <div
                key={theme.name}
                className={`flex items-center justify-between p-3 border rounded-lg cursor-pointer transition-colors ${currentTheme === theme.name
                  ? 'border-primary bg-accent'
                  : 'border-border hover:bg-accent/50'
                  }`}
                onClick={() => applyTheme(theme.name)}
              >
                <div>
                  <div className="font-medium text-foreground">{theme.name}</div>
                  <div className="text-xs text-muted-foreground">
                    {currentTheme === theme.name ? 'Currently active' : 'Click to apply'}
                  </div>
                </div>
                <Tooltip>
                  <TooltipTrigger asChild>
                    <Button
                      variant="outline"
                      size="sm"
                      onClick={(e) => {
                        e.stopPropagation();
                        copyThemeCSS(theme.name);
                      }}
                      className="text-xs"
                    >
                      Copy CSS
                    </Button>
                  </TooltipTrigger>
                  <TooltipContent>
                    <p>Copy Theme CSS</p>
                    <p className="text-xs opacity-80">Copy CSS to clipboard for external use</p>
                  </TooltipContent>
                </Tooltip>
              </div>
            ))}
          </div>
          <div className="text-xs text-muted-foreground">
            💡 You can copy the CSS and paste it into your <code>globals.css</code> file to permanently apply a theme.
          </div>
        </div>
      </DialogContent>
    </Dialog>
  );
}
